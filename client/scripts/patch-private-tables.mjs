#!/usr/bin/env node
/**
 * Patches client/src/module_bindings/index.ts after `spacetime generate`
 * to re-add hand-written private table registrations that codegen removes.
 *
 * Private tables (restore_results, class_recovery_results) are skipped by
 * codegen because SpacetimeDB marks them private. This script is called
 * automatically by `make generate` to keep index.ts complete.
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, resolve } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const INDEX = resolve(__dirname, '../src/module_bindings/index.ts');

let src = readFileSync(INDEX, 'utf8');

// ── Imports ──────────────────────────────────────────────────────────────────
if (!src.includes('ClassRecoveryResultsRow')) {
  src = src.replace(
    '// Import all table schema definitions\n',
    '// Import all table schema definitions\n' +
    '// Hand-written bindings for private tables (codegen skips them):\n' +
    'import ClassRecoveryResultsRow from "./class_recovery_results_table";\n' +
    'import RestoreResultsRow from "./restore_results_table";\n',
  );
  console.log('  + added ClassRecoveryResultsRow / RestoreResultsRow imports');
}

// ── tablesSchema entries ──────────────────────────────────────────────────────
if (!src.includes('restore_results:')) {
  const PRIVATE_TABLES =
`  // Private tables — hand-written, not generated by codegen.
  // Rows are pushed to the owning identity only (server-enforced privacy).
  class_recovery_results: __table({
    name: 'class_recovery_results',
    indexes: [
      { name: 'member_identity', algorithm: 'btree', columns: ['memberIdentity'] },
    ],
    constraints: [
      { name: 'class_recovery_results_member_identity_key', constraint: 'unique', columns: ['memberIdentity'] },
    ],
  }, ClassRecoveryResultsRow),
  restore_results: __table({
    name: 'restore_results',
    indexes: [
      { name: 'caller', algorithm: 'btree', columns: ['caller'] },
    ],
    constraints: [
      { name: 'restore_results_caller_key', constraint: 'unique', columns: ['caller'] },
    ],
  }, RestoreResultsRow),
`;
  src = src.replace('  best_scores: __table({', PRIVATE_TABLES + '  best_scores: __table({');
  console.log('  + added restore_results / class_recovery_results table schemas');
}

writeFileSync(INDEX, src);
console.log('✓ patch-private-tables: index.ts is up to date');
