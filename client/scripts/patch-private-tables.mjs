#!/usr/bin/env node
/**
 * Patches client/src/module_bindings/index.ts after `spacetime generate`
 * to re-add hand-written private table registrations that codegen removes.
 *
 * Only class_recovery_results remains private (ACCT-04).
 * restore_results was made public (ACCT-03) so codegen handles it automatically
 * — same workaround as IssuedProblemResult (SEC-10, SpacetimeDB 2.0.3 limitation:
 *   private tables cannot push rows to subscribers).
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, resolve } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const INDEX = resolve(__dirname, '../src/module_bindings/index.ts');

let src = readFileSync(INDEX, 'utf8');
let changed = false;

// ── Import ────────────────────────────────────────────────────────────────────
if (!src.includes('ClassRecoveryResultsRow')) {
  src = src.replace(
    '// Import all table schema definitions\n',
    '// Import all table schema definitions\n' +
    '// Hand-written binding for private table class_recovery_results (codegen skips it):\n' +
    'import ClassRecoveryResultsRow from "./class_recovery_results_table";\n',
  );
  console.log('  + added ClassRecoveryResultsRow import');
  changed = true;
}

// ── tablesSchema entry ────────────────────────────────────────────────────────
if (!src.includes('class_recovery_results:')) {
  const ENTRY =
`  // Private table — hand-written, not generated by codegen (ACCT-04).
  // SpacetimeDB 2.0.3: private tables cannot push rows to subscribers.
  // class_recovery_results stays private because its rows (student recovery
  // codes) are more sensitive than a short-lived restore token; the teacher
  // reads them synchronously via a dedicated fetch flow.
  class_recovery_results: __table({
    name: 'class_recovery_results',
    indexes: [
      { name: 'member_identity', algorithm: 'btree', columns: ['memberIdentity'] },
    ],
    constraints: [
      { name: 'class_recovery_results_member_identity_key', constraint: 'unique', columns: ['memberIdentity'] },
    ],
  }, ClassRecoveryResultsRow),
`;
  src = src.replace('  best_scores: __table({', ENTRY + '  best_scores: __table({');
  console.log('  + added class_recovery_results table schema');
  changed = true;
}

writeFileSync(INDEX, src);
if (changed) {
  console.log('✓ patch-private-tables: index.ts patched');
} else {
  console.log('✓ patch-private-tables: index.ts already up to date');
}
